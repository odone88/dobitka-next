export function getDailyFacts(count = 3): string[] {
  const day = Math.floor(Date.now() / 86400000); // day index since epoch
  const start = (day * count) % FACTS.length;
  const result: string[] = [];
  for (let i = 0; i < count; i++) {
    result.push(FACTS[(start + i) % FACTS.length]);
  }
  return result;
}
